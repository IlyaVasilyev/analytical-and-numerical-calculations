function polyCoordFrame
  syms e x y z a arg1 arg2 arg3 arg4 Nlocal11 Nlocal12 Nlocal13 Nlocal21 Nlocal22 Nlocal23 Nlocal31 Nlocal32 Nlocal33
  U = x*sin(a*z)+y*cos(a*z);
  V = x*cos(a*z)-y*sin(a*z);
  N = [e+a*V^2 -a^2*U*V -a*V;-a^2*U*V e+a*U^2 a*U;-a*V a*U e];
% det = sym('det(arg1, arg2, arg3, arg4)');
  det(arg1, arg2, arg3, arg4) = arg1*arg4-arg2*arg3;
  det3(Nlocal11, Nlocal12, Nlocal13, Nlocal21, Nlocal22, Nlocal23, Nlocal31, Nlocal32, Nlocal33) = ...
Nlocal11*det(Nlocal22,Nlocal23,Nlocal32,Nlocal33)...
-Nlocal12*det(Nlocal21,Nlocal23,Nlocal31,Nlocal33)...
+Nlocal13*det(Nlocal21,Nlocal22,Nlocal31,Nlocal32)...
-Nlocal21*det(Nlocal12,Nlocal13,Nlocal32,Nlocal33)...
+Nlocal22*det(Nlocal11,Nlocal13,Nlocal31,Nlocal33)...
-Nlocal23*det(Nlocal11,Nlocal12,Nlocal31,Nlocal32)...
+Nlocal31*det(Nlocal12,Nlocal13,Nlocal22,Nlocal23)...
-Nlocal32*det(Nlocal11,Nlocal13,Nlocal21,Nlocal23)...
+Nlocal33*det(Nlocal11,Nlocal12,Nlocal21,Nlocal22);
 C = [N(1,1)*det(N(2,2),N(2,3),N(3,2),N(3,3)) -N(1,2)*det(N(2,1),N(2,3),N(3,1),N(3,3)) N(1,3)*det(N(2,1),N(2,2),N(3,1),N(3,2));...
-N(2,1)*det(N(1,2),N(1,3),N(3,2),N(3,3)) N(2,2)*det(N(1,1),N(1,3),N(3,1),N(3,3)) -N(2,3)*det(N(1,1),N(1,2),N(3,1),N(3,2));...
N(3,1)*det(N(1,2),N(1,3),N(2,2),N(2,3)) -N(3,2)*det(N(1,1),N(1,3),N(2,1),N(2,3)) N(3,3)*det(N(1,1),N(1,2),N(2,1),N(2,2))];
    simplify(expand(det3(C(1,1), C(1,2), C(1,3), C(2,1), C(2,2), C(2,3), C(3,1), C(3,2), C(3,3)),'ArithmeticOnly',false,'IgnoreAnalyticConstraints',true))
    %expand((x*sin(a*z)+y*cos(a*z))^2+(x*cos(a*z)-y*sin(a*z))^2,'IgnoreAnalyticConstraints',true)


